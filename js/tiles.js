class Rect {
    constructor(x, y, w, h) {
        this.x = x
        this.y = y
        this.w = w
        this.h = h
    }
}

class Circle {
    constructor(x, y, r) {
        this.x = x
        this.y = y
        this.r = r
    }
}

class TileLocation {
    constructor(x, y) {
        this.x = x
        this.y = y
    }
}